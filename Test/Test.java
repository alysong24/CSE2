public class Test {
    
    public static void main(String[] args) {
        
        int counter = 0;
        int two = 6;
        while (counter < 3 ) {
            counter++;
        }
        System.out.println("Counter: " + counter);
        
        if (two == 6) {
            two++;
        }
        else {
            two--;
        }
        System.out.println(two);
    }
}